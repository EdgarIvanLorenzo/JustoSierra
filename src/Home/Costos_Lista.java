    private void ObtenerCostos() {
        int contador = 0;
        ArrayList<Costos> costos = new ArrayList<>();
        costos = crud1.getCostos();
        //Recorridos de los grupos
        for (Costos c : costos) {
            if (c.getNombre().equals("Inscripcion") || c.getNombre().equals("Colegiatura")|| c.getNombre().equals("Reinscripcion")) {
                this.filas[contador][0] = (Object) c.getIdCostos();
                this.filas[contador][1] = (Object) c.getNombre();
                this.filas[contador][2] = (Object) c.getCantidad();
                this.filas[contador][3] = (Object) modificar;
            } else {
                this.filas[contador][0] = (Object) c.getIdCostos();
                this.filas[contador][1] = (Object) c.getNombre();
                this.filas[contador][2] = (Object) c.getCantidad();
                this.filas[contador][3] = (Object) modificar;
                this.filas[contador][4] = (Object) eliminar;
            }
            contador++;
        }
    }

