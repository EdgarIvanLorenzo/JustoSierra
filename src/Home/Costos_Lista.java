    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        int columna = tabla.getColumnModel().getColumnIndexAtX(evt.getX());
        int fila = evt.getY() / tabla.getRowHeight();

        if (fila < tabla.getRowCount() && fila >= 0 && columna < tabla.getColumnCount() && columna >= 0) {
            Object valor = tabla.getValueAt(fila, columna);
            if (valor instanceof JButton) {
                ((JButton) valor).doClick();
                JButton boton = (JButton) valor;
                int id = (int) tabla.getValueAt(fila, 0);
                String nombre = (String) tabla.getValueAt(fila, 1);
                String cantidad = (String) tabla.getValueAt(fila, 2);
                if (boton.getName().equals("m")) {
                    Costos_Nuevo g = new Costos_Nuevo(id, "Modificar", nombre, cantidad);
                    g.setVisible(true);
                    this.setVisible(false);
                } else if (boton.getName().equals("e")) {
                    crud1.deleteCosto(id);
                    this.ConstruirTabla();
                }
            }
        }
    }//GEN-LAST:event_tablaMouseClicked

